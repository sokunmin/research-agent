@dataclass
class CodeEvalResult:
    layout_lookup_correct: bool
    save_path_correct: bool
    null_guard_correct: bool
    import_correct: bool

    @property
    def overall_correct(self) -> bool:
        return (self.layout_lookup_correct and self.save_path_correct
                and self.null_guard_correct and self.import_correct)

    @property
    def score(self) -> int:
        """Number of checks passed (0–4)."""
        return sum([self.layout_lookup_correct, self.save_path_correct,
                    self.null_guard_correct, self.import_correct])


@dataclass
class TestCase:
    id: str
    label: str
    slides: list


@dataclass
class PromptVariant:
    id: str
    label: str
    template: str
    description: str
    rationale: str


@dataclass
class RunResult:
    model_label: str
    prompt_id: str
    test_case_id: str
    run_idx: int
    generated_code: str
    eval_result: CodeEvalResult
    elapsed_s: float
    error: Optional[str]

    def to_dict(self) -> dict:
        return {
            "model_label":    self.model_label,
            "prompt_id":      self.prompt_id,
            "test_case_id":   self.test_case_id,
            "run_idx":        self.run_idx,
            "generated_code": self.generated_code,
            "eval": {
                "layout_lookup_correct": self.eval_result.layout_lookup_correct,
                "save_path_correct":     self.eval_result.save_path_correct,
                "null_guard_correct":    self.eval_result.null_guard_correct,
                "import_correct":        self.eval_result.import_correct,
                "overall_correct":       self.eval_result.overall_correct,
                "score":                 self.eval_result.score,
            },
            "elapsed_s": self.elapsed_s,
            "error":     self.error,
        }


