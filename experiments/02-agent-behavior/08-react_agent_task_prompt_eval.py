def _check_model_available(model: dict) -> bool:
    """Quick ping to verify model is reachable before running full experiment."""
    try:
        resp = litellm.completion(
            model=model["name"],
            messages=[{"role": "user", "content": "Reply with exactly: OK"}],
            max_tokens=50,
            timeout=30,
        )
        content = resp.choices[0].message.content or ""
        return len(content) > 0
    except Exception as e:
        print(f"  Availability check failed: {e}")
        return False


# ══════════════════════════════════════════════════════════════════════════════
# Main
# ══════════════════════════════════════════════════════════════════════════════

def main() -> None:
    evaluator = CodeEvaluator()
    reporter  = ResultReporter()

    active_prompt_ids = RUN_PROMPT_IDS or [p.id for p in PROMPTS]
    total_calls = (
        len(MODELS) * len(active_prompt_ids) * len(TEST_CASES) * N_RUNS
    )

    print(f"\n{'='*70}")
    print(f"  Slide Gen Prompt Engineering Experiment")
    print(f"  Prompts : {active_prompt_ids}")
    print(f"  Models  : {[m['label'] for m in MODELS]}")
    print(f"  N_RUNS  : {N_RUNS}  |  Total LLM calls (max): {total_calls}")
    print(f"  Output  : {RESULTS_PATH}")
    print(f"{'='*70}")

    runner = ExperimentRunner(
        models=MODELS,
        prompts=PROMPTS,
        test_cases=TEST_CASES,
        n_runs=N_RUNS,
        evaluator=evaluator,
        run_prompt_ids=RUN_PROMPT_IDS,
    )

    results = runner.run_all()

    if not results:
        print("\nNo results — all models unavailable or no prompts selected.")
        return

    reporter.print_summary(results)
    reporter.print_check_breakdown(results)
    reporter.save_json(results, RESULTS_PATH)


if __name__ == "__main__":
    main()
