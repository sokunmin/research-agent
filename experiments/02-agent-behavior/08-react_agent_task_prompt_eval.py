class ExperimentRunner:
    """Orchestrates runs: (model × prompt × test_case) × N_RUNS, sequentially."""

    def __init__(
        self,
        models: list[dict],
        prompts: list[PromptVariant],
        test_cases: list[TestCase],
        n_runs: int,
        evaluator: CodeEvaluator,
        run_prompt_ids: Optional[list[str]] = None,
    ) -> None:
        self._models       = models
        self._test_cases   = test_cases
        self._n_runs       = n_runs
        self._evaluator    = evaluator
        self._prompts      = (
            [p for p in prompts if p.id in run_prompt_ids]
            if run_prompt_ids is not None else prompts
        )

    def run_all(self) -> list[RunResult]:
        all_results: list[RunResult] = []
        for model in self._models:
            print(f"\n{'='*70}")
            print(f"  Checking availability: {model['name']}")
            if not _check_model_available(model):
                print(f"  SKIP — model not available: {model['name']}")
                print(f"{'='*70}")
                continue
            print(f"  OK — model available")
            print(f"{'='*70}")

            for prompt in self._prompts:
                for tc in self._test_cases:
                    for run_idx in range(1, self._n_runs + 1):
                        result = self._run_single(model, prompt, tc, run_idx)
                        all_results.append(result)

        return all_results

    def _run_single(
        self,
        model: dict,
        prompt: PromptVariant,
        test_case: TestCase,
        run_idx: int,
    ) -> RunResult:
        slide_data_str = json.dumps(test_case.slides, indent=2)
        filled_prompt  = prompt.template.format(slide_data=slide_data_str)

        generated_code = ""
        error: Optional[str] = None
        elapsed = 0.0
        eval_result = CodeEvalResult(False, False, False, False)

        try:
            t0 = time.perf_counter()
            resp = litellm.completion(
                model=model["name"],
                messages=[{"role": "user", "content": filled_prompt}],
                max_tokens=2048,
                temperature=0.1,
                timeout=120,
            )
            elapsed = time.perf_counter() - t0
            generated_code = resp.choices[0].message.content or ""
            eval_result = self._evaluator.evaluate(generated_code)
        except Exception as e:
            elapsed = time.perf_counter() - t0
            error = str(e)[:400]

        # Progress line
        ev = eval_result
        def sym(b: bool) -> str: return "✓" if b else "✗"
        print(
            f"[{model['label']}] {prompt.id} / {test_case.id}"
            f"  run {run_idx}/{self._n_runs} ..."
            f"  layout={sym(ev.layout_lookup_correct)}"
            f"  save={sym(ev.save_path_correct)}"
            f"  null={sym(ev.null_guard_correct)}"
            f"  import={sym(ev.import_correct)}"
            f"  ({elapsed:.1f}s)"
            + (f"  ERROR: {error[:60]}" if error else "")
        )

        return RunResult(
            model_label=model["label"],
            prompt_id=prompt.id,
            test_case_id=test_case.id,
            run_idx=run_idx,
            generated_code=generated_code,
            eval_result=eval_result,
            elapsed_s=round(elapsed, 2),
            error=error,
        )


