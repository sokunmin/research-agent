# ResultReporter
# ══════════════════════════════════════════════════════════════════════════════

class ResultReporter:
    """Formats and prints aggregated results. No computation — display only."""

    def print_summary(self, results: list[RunResult]) -> None:
        """Print per-(model × prompt × test_case) aggregated table."""
        if not results:
            print("No results to display.")
            return

        models = list(dict.fromkeys(r.model_label for r in results))

        print()
        for model_label in models:
            model_results = [r for r in results if r.model_label == model_label]
            print(f"MODEL: {model_label}")
            print("─" * 75)
            print(f"{'prompt':<22}  {'test_case':<18}  {'N':>2}  "
                  f"{'layout%':>8}  {'save%':>6}  {'null%':>6}  "
                  f"{'import%':>7}  {'overall%':>8}")
            print("─" * 75)

            prompts_seen = list(dict.fromkeys(r.prompt_id for r in model_results))
            tcs_seen     = list(dict.fromkeys(r.test_case_id for r in model_results))

            for prompt_id in prompts_seen:
                for tc_id in tcs_seen:
                    group = [
                        r for r in model_results
                        if r.prompt_id == prompt_id and r.test_case_id == tc_id
                    ]
                    if not group:
                        continue
                    n = len(group)
                    def pct(key: str) -> float:
                        return 100.0 * sum(getattr(r.eval_result, key) for r in group) / n
                    print(
                        f"{prompt_id:<22}  {tc_id:<18}  {n:>2}  "
                        f"{pct('layout_lookup_correct'):>8.1f}  "
                        f"{pct('save_path_correct'):>6.1f}  "
                        f"{pct('null_guard_correct'):>6.1f}  "
                        f"{pct('import_correct'):>7.1f}  "
                        f"{100.0 * sum(r.eval_result.overall_correct for r in group) / n:>8.1f}"
                    )
            print()

    def print_check_breakdown(self, results: list[RunResult]) -> None:
        """Print which specific check fails most often per prompt variant."""
        if not results:
            return

        prompts_seen = list(dict.fromkeys(r.prompt_id for r in results))

        print("CHECK FAILURE BREAKDOWN (across all models and test cases)")
        print("─" * 60)
        print(f"{'prompt':<22}  {'layout':>7}  {'save':>6}  {'null':>6}  {'import':>7}  {'N':>4}")
        print("─" * 60)
        for prompt_id in prompts_seen:
            group = [r for r in results if r.prompt_id == prompt_id]
            n = len(group)
            def fail_pct(key: str) -> float:
                return 100.0 * sum(not getattr(r.eval_result, key) for r in group) / n
            print(
                f"{prompt_id:<22}  "
                f"{fail_pct('layout_lookup_correct'):>7.1f}  "
                f"{fail_pct('save_path_correct'):>6.1f}  "
                f"{fail_pct('null_guard_correct'):>6.1f}  "
                f"{fail_pct('import_correct'):>7.1f}  "
                f"{n:>4}"
            )
        print()

    def save_json(self, results: list[RunResult], path: str) -> None:
        """Append-merge with existing results JSON if file exists, then save."""
        existing: list[dict] = []
        if Path(path).exists():
            with open(path) as f:
                existing = json.load(f)
        all_results = existing + [r.to_dict() for r in results]
        with open(path, "w") as f:
            json.dump(all_results, f, indent=2)
        print(f"Results saved to {path}  (total entries: {len(all_results)})")


